       <p>Where elementary school Math Olympiad is pretty gender balanced, Math Club at Middle School was full of very confident and competitive boys, and few if any girls.   We first met together as a Circle of four or five 6th graders, in each other’s homes, a different living room or kitchen each week, in hopes of addressing a retention problem:  how could we feel like a girl powerhouse in a male-dominated environment?  Our founding members in that small group included Miryam Ginsparg, Hannah Han, Lucy Wang, Stella Whittaker, and Lisa Yoo. Within a few meetings, more girls joined, and by June, our membership was over a dozen, including girls in grades 6 through 8.  
		</p>
		<p>In spring 2012, we began to focus on making our girls competitive at various competitions, including MathCounts, and placed the first of many girls in the top 10 at the Regional MathCounts Competitions.  We now place one or more girls among the top 10 competitors every year.  By fall 2013, we had grown large enough to move to a home at Cornell University, usually sponsored by a faculty parent.    We moved to our current home in Corson Hall in the Fall of 2014, and became an outreach project of the Cornell Math Department in the Winter of 2015. Our attendance now includes up to 25 girls from grades 6 to 12, plus math graduate student helpers.   
		</p>
		<p>Our girls apply to and attend summer Math Programs,  such as Math Path and Awesome Math, and  are involved in Peer Tutoring through the Math Circle Girl2Girl  math tutoring effort.
